// Todo10: Calculate the area of a circle given its radius.
// Formula: Area = PI * radius * radius

void main() {
  double radius = 35; //mm
  double pi = 3.145;
  double areaOfCircle = pi * (radius * radius);
  print('Area of Circle is: $areaOfCircle');
}
