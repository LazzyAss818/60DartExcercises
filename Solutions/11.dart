// Todo11: Write a Dart program to check if a number is even or odd.

void main() {
  int number = 23;
  // If its divisible by 2 it's an even, and if not then it is odd.
  if (number % 2 == 0) {
    print('$number is an even number.');
  } else {
    print('$number is an odd number.');
  }
}
